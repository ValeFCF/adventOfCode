func howManyTrees(input: String, stepDown: Int, stepRight: Int) -> Int {
    let givenArray = input.components(separatedBy: .newlines)
    var rowArray = input.components(separatedBy: .newlines)

    let howManyNeed = rowArray.count * stepRight
    let multiplier = howManyNeed / rowArray[0].count
    
    for (index, _) in rowArray.enumerated() {
        for _ in 0...multiplier + 1 {
            rowArray[index].append(contentsOf: givenArray[index])
        }
    }
    
    var glIndex = stepDown
    var glX = stepRight
    var trees = 0
    while glIndex < rowArray.count {
        
        let text = rowArray[glIndex]
        let index2 = text.index(text.startIndex, offsetBy: glX)
        let lastChar: Character = text[index2]
        
        if lastChar == "#" {
            trees += 1
        }
        
        glIndex += stepDown
        glX += stepRight
    }
    
    return trees
}

